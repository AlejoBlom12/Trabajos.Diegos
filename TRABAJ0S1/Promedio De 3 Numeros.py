#INFORMACION DEL PROGRAMA:

'''Suma de 3 numeros ingresados por  teclado  y calcular el promedio'''

#VARIABLES:

x = float(input("Ingrese el primer numero: "))

print("--------------------------------------")

y = float(input("Ingrese el segundo numero: "))

print("--------------------------------------")

z = float(input("Ingrese el tercer numero: "))

print("----------------------------------------------------------")

#ECUACION PRINCIPAL: 

promedio = (x + y + z)/3

#IMPRESION:

print("El promedio de los tres numeros asignados es = " + str(promedio))
print("----------------------------------------------------------")